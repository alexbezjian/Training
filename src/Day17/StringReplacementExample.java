package Day17;

public class StringReplacementExample {
    public static void main(String[] args) {
        System.out.println("SOB" + "BALLOT");

       System.out.println(14+29);
       System.out.println(7+2 + "2684");

        String name = "QWERTY";
        name.replace("Q", "Y");
        System.out.println(name);


        String name2 = "GYIO";
        String name3 =  name2.replace("O", "A");
        System.out.println(name3);



    }
}
